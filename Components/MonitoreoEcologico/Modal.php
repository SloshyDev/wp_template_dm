                    <td class="tg-buh4">Methods:</td>
                    <td class="tg-buh4">Data are collected using transect belt methods, using the standardized
                        methodology of the Long-Term Ecological Monitoring Program designed by the Gulf of
                        California Marine Program.&nbsp;&nbsp;Divers use SCUBA diving in rocky reefs and use linear
                        transects (fish = 50m x 5m; invertebrates = 30m x 1m) as a guide. Fish: Transects were
                        placed at 20 and 5 meters depth. If the sites monitored does not have a rocky bottom at more
                        than 5 meters, only one depth is surveyed. Fishes are visually counted over a belt transect
                        with a reference area of 50m x 5m: first swim over transect, larger demersal fishes are
                        prioritized, whereas on the second swim the focus is on cryptic species. Therefore, the
                        final estimation area is of 250 m squared. Fish sizes are also visually estimated, this
                        measures is then combined to bibliographic data to estimate biomass.Invertebrates: Transects
                        were placed at&nbsp;&nbsp;20 and 5 meters depth. Invertebrates are visually counted over a
                        belt transect with a reference area of 30m x 1m: one swim over transect counting epibenthic
                        macroinvertebrates (larger than 2 cm) and recording estimated size.</td>
