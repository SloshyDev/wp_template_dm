                    <td class="tg-buh4">Methods:</td>
                    <td class="tg-buh4">Los datos se generan empleando metodologías estandarizadas adaptadas para el
                        programa de monitoreo de largo plazo del Programa Marino del Golfo de California. Los buzos
                        utilizan transectos como guia (peces=50m x 5m; invertebrados=30m x 1m) durante el monitoreo.
                        Peces: Los transectos se colocan a 20m y 5m de profundidad. Si el sitio de monitoreo no
                        cuenta con fondos rocosos más allá de los 5m de profundidad, esta es la única profundidad
                        que se monitorea. Los peces se cuentan sobre el transecto contando primero los peces
                        demeersales y, en la segunda vuelta, se cuentan los peces bentónicos y crípticos. El área
                        monitoreada total es de 250m cuadrados. Las tallas de los peces también se registra y, junto
                        con información de bibliografía, se utiliza para calcular la biomasa.Invertebrados: Los
                        transectos se colocan a 5m y 20 m de profundidad y se registran las especies sobre el
                        transecto. El buzo cuenta macroinvertebrados epibentónicos (mayores a 2cm) y anota la talla
                        estimada.</td>
